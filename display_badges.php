    $res = get_tweets_badge($user);
    if ($res != "") $badges .= "&nbsp;&nbsp;" . $res;

    $res = get_image_badge($user);
    if ($res != "") $badges .= "&nbsp;&nbsp;" . $res;

    return $badges;
}

