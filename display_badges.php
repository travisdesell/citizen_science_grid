        $badges .= "&nbsp;&nbsp;";
        $badges .= get_event_badge($user);
    }

    $res = get_wildlife_credit_badge($user);
    if ($res != "") $badges .= "&nbsp;&nbsp;" . $res;

    $res = get_dna_credit_badge($user);
    if ($res != "") $badges .= "&nbsp;&nbsp;" . $res;

    $res = get_sss_credit_badge($user);
